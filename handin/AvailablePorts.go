import "sync"

type SafeIterator struct {
	mu sync.Mutex
	index int
	values []string
}

func (i *SafeIterator) Next() string {
	i.mu.Lock()
	defer i.mu.Unlock()
	i.index += 1
	return i.values[i.index]
}


